import Papa from "papaparse";

export type ColumnType = "numeric" | "categorical";

export interface DatasetColumn {
  name: string;
  type: ColumnType;
  missing: number;
  uniqueCount: number;
  sample: (string | number | null)[];
}

export interface Dataset {
  fileName: string;
  rows: Record<string, string | number | null>[];
  columns: DatasetColumn[];
  rowCount: number;
}

export function parseCsv(file: File): Promise<Dataset> {
  return new Promise((resolve, reject) => {
    Papa.parse<Record<string, string>>(file, {
      header: true,
      skipEmptyLines: true,
      dynamicTyping: false,
      complete: (results) => {
        if (results.errors.length > 0 && results.data.length === 0) {
          reject(new Error(results.errors[0].message));
          return;
        }
        const fields = results.meta.fields ?? [];
        const rawRows = results.data;

        const rows: Record<string, string | number | null>[] = [];
        for (const r of rawRows) {
          const out: Record<string, string | number | null> = {};
          for (const f of fields) {
            const raw = r[f];
            if (raw === undefined || raw === null || raw === "") {
              out[f] = null;
            } else {
              out[f] = raw;
            }
          }
          rows.push(out);
        }

        const columns: DatasetColumn[] = fields.map((name) => {
          const colVals = rows.map((r) => r[name]);
          let numericCount = 0;
          let nonMissing = 0;
          for (const v of colVals) {
            if (v === null || v === undefined) continue;
            nonMissing++;
            if (typeof v === "number" || (!isNaN(Number(v)) && v !== "")) {
              numericCount++;
            }
          }
          const isNumeric = nonMissing > 0 && numericCount / nonMissing > 0.85;
          const type: ColumnType = isNumeric ? "numeric" : "categorical";

          // Convert column to right type in-place
          for (const r of rows) {
            const v = r[name];
            if (v === null || v === undefined || v === "") {
              r[name] = null;
            } else if (type === "numeric") {
              const n = Number(v);
              r[name] = isNaN(n) ? null : n;
            } else {
              r[name] = String(v);
            }
          }

          const present = colVals.filter((v) => v !== null && v !== undefined);
          const missing = colVals.length - present.length;
          const uniqueCount = new Set(
            colVals
              .filter((v) => v !== null && v !== undefined)
              .map((v) => String(v)),
          ).size;
          return {
            name,
            type,
            missing,
            uniqueCount,
            sample: colVals.slice(0, 5),
          };
        });

        resolve({
          fileName: file.name,
          rows,
          columns,
          rowCount: rows.length,
        });
      },
      error: (err) => reject(err),
    });
  });
}

export function getColumnValues(
  ds: Dataset,
  name: string,
): (string | number | null)[] {
  return ds.rows.map((r) => r[name]);
}

export function getNumericValues(ds: Dataset, name: string): number[] {
  const out: number[] = [];
  for (const r of ds.rows) {
    const v = r[name];
    if (typeof v === "number") out.push(v);
  }
  return out;
}

export function uniqueCategories(
  ds: Dataset,
  name: string,
): string[] {
  const set = new Set<string>();
  for (const r of ds.rows) {
    const v = r[name];
    if (v !== null && v !== undefined) set.add(String(v));
  }
  return Array.from(set).sort();
}

